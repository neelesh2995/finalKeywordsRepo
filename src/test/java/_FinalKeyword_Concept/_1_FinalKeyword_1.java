package _FinalKeyword_Concept;

public class _1_FinalKeyword_1 {
	
	
	public static void main(String neil[])   {
		
		
		int a = 10;
		
		a = 20;  //most recent change will be the output
		
		System.out.println(a);   // out put is 20
		
		
		// By declaring a variable final its value cannot be overridden
	
	    
		final int b = 10;
		
	//	b = 20;
		
		System.out.println(b);
	
	
	
	}

}
